#include "../libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	size_t	a;

	a = 0;
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", fd);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', fd);
			a = -n;
		}
		else
			a = n;
		if (a > 9)
			ft_putnbr_fd(a / 10, fd);
		ft_putchar_fd(a % 10 + 48, fd);
	}
}
